package game;

public class Controls {
    public int up, left, down, right, shoot, dash;
    
    public Controls(int up, int left, int down, int right, int shoot, int dash) {
        this.up = up;
        this.left = left;
        this.down = down;
        this.right = right;
        this.shoot = shoot;
        this.dash = dash;
    }
}